package vr.projects.sunnycompass.berrytracker.domain;

/**
 * CategoryType defines possible 'types' of categories
 *
 * <ol>
 *   <li>SOURCE : defines the types which are incoming/credit in nature
 *   <li>SINK : defines the types which are outgoing/debit in nature
 * </ol>
 */
public enum CategoryType {
    SOURCE,
    SINK
}
